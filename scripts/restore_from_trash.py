import os
import shutil
from pathlib import Path

def main():
    script_dir = Path(__file__).parent
    project_root = script_dir.parent
    
    # Define Trash and Destination paths
    trash_dir = project_root / 'dataset' / 'drive_trash'
    dataset_root = project_root / 'dataset'
    
    processed_anno_dir = dataset_root / 'processed_dataset' / 'Anno'
    removed_anno_dir = dataset_root / 'removed' / 'Anno'
    processed_img_dir = dataset_root / 'processed_dataset' / 'Image'
    removed_img_dir = dataset_root / 'removed' / 'Image'

    # 1. Restore Metadata Files first
    print("--- Step 1: Restoring Metadata ---")
    for txt_name in ['removed.txt', 'weird_bbox.txt']:
        found = list(trash_dir.rglob(txt_name))
        if found:
            shutil.copy2(found[0], dataset_root / txt_name)
            print(f"Restored {txt_name} to {dataset_root}")

    # Load removed list to know where to put files
    removed_names = set()
    removed_txt_path = dataset_root / 'removed.txt'
    if removed_txt_path.exists():
        with open(removed_txt_path, 'r', encoding='utf-8') as f:
            removed_names = {line.strip() for line in f if line.strip()}

    # 2. Restore Annotations from Trash
    print("\n--- Step 2: Restoring Annotations ---")
    os.makedirs(processed_anno_dir, exist_ok=True)
    os.makedirs(removed_anno_dir, exist_ok=True)
    
    json_files = list(trash_dir.rglob('*.json'))
    restored_count = 0
    
    for json_file in json_files:
        # Ignore the manifest if it's in the trash
        if json_file.name == 'pairtally_annotations_simple.json':
            continue
            
        # Determine if this belongs in 'removed'
        # We check if the corresponding .jpg is in the removed list
        img_name_guess = json_file.name.replace('.json', '.jpg')
        
        if img_name_guess in removed_names:
            dest = removed_anno_dir / json_file.name
        else:
            dest = processed_anno_dir / json_file.name
            
        shutil.copy2(json_file, dest)
        restored_count += 1

    print(f"Restored {restored_count} annotation files.")

    # 3. Handle Images (Post-Processing)
    # This part moves images from 'processed_dataset/Image' to 'removed/Image'
    # only if they already exist in the processed folder.
    print("\n--- Step 3: Organizing Images ---")
    os.makedirs(removed_img_dir, exist_ok=True)
    os.makedirs(removed_anno_dir, exist_ok=True)
    moved_img_count = 0
    moved_anno_count = 0
    
    # Create sets of names that should be in 'removed'
    removed_image_names_set = set()
    removed_json_names_set = set()
    for name in removed_names: # removed_names contains image filenames like 'xxx_positive.jpg'
        if name.endswith(('.jpg', '.jpeg', '.png')):
            removed_image_names_set.add(name)
            removed_json_names_set.add(Path(name).stem + ".json")

    # Iterate through files currently in processed_dataset/Image
    for current_img_path in list(processed_img_dir.glob('*')): # Use list() to avoid issues during iteration
        if current_img_path.name in removed_image_names_set:
            shutil.move(str(current_img_path), str(removed_img_dir / current_img_path.name))
            moved_img_count += 1

    # Iterate through files currently in processed_dataset/Anno
    for current_json_path in list(processed_anno_dir.glob('*')): # Use list() to avoid issues during iteration
        if current_json_path.name in removed_json_names_set:
            dest_json_path = removed_anno_dir / current_json_path.name
            if dest_json_path.exists():
                # If the annotation already exists in 'removed' (restored in Step 2),
                # it means the one in 'processed' is a duplicate generated by create_dataset.py.
                # Delete the duplicate from 'processed'.
                os.remove(current_json_path)
            else:
                shutil.move(str(current_json_path), str(dest_json_path))
                moved_anno_count += 1
    
    print(f"Organized {moved_img_count} images and {moved_anno_count} annotations based on 'removed.txt'.")
    
    print("\n--- Restoration Summary ---")
    print(f"Annotations Restored: {restored_count}")
    print(f"Files Organized:     {moved_img_count + moved_anno_count}")
    print("\nNext Steps:")
    print("1. If images are still missing, download the source images to 'dataset/pairtally_dataset/images'")
    print("2. Run 'python scripts/create_dataset.py'")
    print("3. Run this script AGAIN to move 'removed' images to their correct folder.")

if __name__ == "__main__":
    main()